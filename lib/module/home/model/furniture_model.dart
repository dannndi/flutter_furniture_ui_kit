/// our class model that hold our data
class FurnitureModel {
  final String name;
  final String assets;
  final double rating;
  final double price;
  final int sold;
  final int reviews;
  final String desc;

  FurnitureModel({
    required this.name,
    required this.assets,
    required this.rating,
    required this.price,
    required this.sold,
    required this.reviews,
    required this.desc,
  });
}

